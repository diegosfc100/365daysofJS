/* Decremente um número com JavaScript

Você pode facilmente decrementar ou diminuir uma variável com o operador --.

i--;

é o equivalente a

i = i - 1;

Nota:
A linha inteira se torna i--; eliminando a necessidade do sinal de igual.

Altere o código para usar o operador -- em myVar:

var myVar = 11;

EXERCÍCIO RESOLVIDO ABAIXO: */ 

var myVar = myVar - 1;
myVar--;

//

var decrementando = 100
decrementando-- // 99

