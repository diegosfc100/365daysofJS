/* Incrementar um número com JavaScript

Você pode facilmente incrementar ou adicionar um a uma variável com o operador ++.

i ++;

é o equivalente a

i = i + 1;

Nota:
A linha inteira se torna i ++; eliminando a necessidade do sinal de igual.

Altere o código para usar o operador ++ em myVar:

var myVar = 87;

EXERCÍCIO RESOLVIDO ABAIXO: */ 

var myVar = myVar + 1;
myVar++;

//

var idade = 31;


console.log(idade++) // primeiro irá imprimir idade(31) e depois ira incrementar(32)
console.log(idade++) // 32
console.log(idade++) // 33
console.log(idade) // 34

//

var ano = 2021;
console.log(ano);

ano++ // 2022
console.log(ano);

