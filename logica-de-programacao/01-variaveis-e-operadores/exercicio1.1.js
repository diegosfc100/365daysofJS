var number = prompt("Digite um número:")                 
number = parseInt(number);  

var number2 = prompt("Digite outro número")                    
number2 = parseInt(number2);                                   

alert(`a subtração de ${number} - ${number2} é igual a ${number - number2}`)
alert(`a multiplicação de ${number} * ${number2} é igual a ${number * number2}`)
alert(`a divisão de ${number} / ${number2} é igual a ${number / number2}`)
alert(`a soma de ${number} + ${number2} é igual a ${number + number2}`)