// WHILE
    // estrutura de loop, fica se repetindo enquanto a condição for verdadeira.
    // Enquanto [condição] for verdade, faça isso...
    var counter = 0;
    while(counter < 10){
        console.log(counter);
        counter++;
    }
    // contador decrescente
    var counter2 = 10;
    while(counter2--){
        console.log(counter2);
    }

 