// OPERADORES LÓGICOS:

    // && Operador "and"
    x==3 && y==4    // (x igual a 3 e y igual a 4)

    // || Operador "or"
    x==3 || y==4    // (x igual a 3 ou y igual a 4)

    // ! Operador "not"
    x !== 3 // false
    x !== '3' // true

// + OPERADOR UNARIO
    // usado para converter em número variáveis que são NaN
    +'3' // 3
    // usado para concatenação, sempre que tiver um + somando uma string ele irá retornar uma concatenação
        'cla' + 'ra' // 'clara'
        '3' +  3 // '33'

// - INVERSOR DE SINAL
    // também converte a string para número
        -'3' // -3


