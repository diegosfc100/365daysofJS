// OPERADORES LÓGICOS:
    var x = 3, y = 4;

    // && Operador "and"
    x == 3 && y == 4

    // || Operador "or"
    x == 3 || y == 4 

    // ! Operador "not"
    x !== 3 // false
    x !== '3' // true



// OPERADOR UNARIO
    // usado para converter em número variáveis que são NaN
    +'3' // 3
    +'Diego' // NaN

    // usado para concatenação, sempre que tiver um + somando uma string ele irá retornar uma concatenação
        'cla' + 'ra' // 'clara'
        '3' + 3 // '33'

    // Inversor de sinal
    var a = '5'
    -a // -5