/* Concatenando Strings com Operador Plus

Em JavaScript, quando o operador + é usado com um valor String, ele é chamado de operador de concatenação.
Você pode construir uma nova string a partir de outras strings concatenando-as.

Exemplo: */

'Meu nome é Alan,' + 'Eu concateno.'

/*Nota:
Cuidado com os espaços. A concatenação não adiciona espaços entre as strings concatenadas, portanto,
você mesmo precisará adicioná-los.

Exemplo: */

var ourStr = "Eu venho primeiro." + "Eu venho em segundo lugar.";
// ourStr é "Eu venho primeiro. Eu venho em segundo lugar." */

/* Crie myStr a partir das cadeias de caracteres "Este é o início" e "Este é o fim". usando o operador +. 

EXERCÍCIO RESOLVIDO ABAIXO: */ 

