/* Anexando Variáveis a Strings

Assim como podemos construir uma string em várias linhas de literais de string,
também podemos anexar variáveis a uma string usando o operador mais igual (+=).

Exemplo: */

var anAdjective = "awesome!";
var ourStr = "freeCodeCamp is";
ourStr += anAdjetivo;
// ourStr agora é "freeCodeCamp is awesome!"

