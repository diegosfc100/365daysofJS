var n1 = 99, n2 = 1;

console.log(n1 == n2);
console.log(n1 != n2);
console.log(n1 > n2);
console.log(n1 < n2);
console.log(n1 >= n2);
console.log(n1 <= n2);
console.log(n1 === n2);
console.log(n1 !== n2);