/* Armazenando Valores com o Operador de Atribuição

Em JavaScript, você pode armazenar um valor em uma variável com o operador de atribuição (=).

myVariable = 5;

Isso atribui o valor numérico 5 a myVariable.
Se houver cálculos à direita do operador =, eles serão executados antes que o valor seja
atribuído à variável à esquerda do operador.

var myVar;
myVar = 5;

Primeiro, este código cria uma variável chamada myVar. Em seguida, o código atribui 5 a myVar.
Agora, se myVar aparecer novamente no código, o programa o tratará como se fosse 5.

Atribua o valor 7 à variável a.

EXERCÍCIO RESOLVIDO ABAIXO: */ 

var a;
a = 7;
